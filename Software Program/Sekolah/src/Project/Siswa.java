    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        try{
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM siswa where "+"no_daftar like '%"+txtcari.getText()+"%' OR "+"nis like '%"+txtcari.getText()+"%'");
            DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("No. Pendaftaran");
        tbl.addColumn("Tanggal Daftar");
        tbl.addColumn("NIS");
        tbl.addColumn("Nama Siswa");
        tbl.addColumn("Tempat Lahir");
        tbl.addColumn("Tanggal Lahir");
        tbl.addColumn("Jenis Kelamin");
        tbl.addColumn("Agama");
        tbl.addColumn("Kelas");
        tbl.addColumn("Nama Wali");
        tbl.addColumn("Alamat");
            tabel.setModel(tbl);
            while(res.next()){
                tbl.addRow(new Object[]{
                res.getString("no_daftar"),
                res.getString("tgl_daftar"),
                res.getString("nis"),
                res.getString("nama_siswa"),
                res.getString("tempat_lahir"),
                res.getString("tgl_lahir"),
                res.getString("jk"),
                res.getString("agama"),
                res.getString("kelas"),
                res.getString("wali"),
                res.getString("alamat"),
                });
                tabel.setModel(tbl);
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(null ,"error");
        }
    }//GEN-LAST:event_bcariActionPerformed

