private void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("No. Pendaftaran");
        tbl.addColumn("Tgl Daftar");
        tbl.addColumn("NIS");
        tbl.addColumn("Nama Siswa");
        tbl.addColumn("Tmpt Lahir");
        tbl.addColumn("Tgl Lahir");
        tbl.addColumn("Jenis Kelamin");
        tbl.addColumn("Agama");
        tbl.addColumn("Kelas");
        tbl.addColumn("Nama Wali");
        tbl.addColumn("Alamat");
        tabel.setModel(tbl);
        try{
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM siswa");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("no_daftar"),
                res.getString("tgl_daftar"),
                res.getString("nis"),
                res.getString("nama_siswa"),
                res.getString("tempat_lahir"),
                res.getString("tgl_lahir"),
                res.getString("jk"),
                res.getString("agama"),
                res.getString("kelas"),
                res.getString("wali"),
                res.getString("alamat"),
                });
            tabel.setModel(tbl);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
            
        }
        
    }
