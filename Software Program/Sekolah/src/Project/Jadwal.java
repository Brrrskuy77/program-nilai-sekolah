private void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("No. ID");
        tbl.addColumn("Kode Pelajaran");
        tbl.addColumn("Mata Pelajaran");
        tbl.addColumn("Kelas");
        tbl.addColumn("NIG");
        tbl.addColumn("Nama Guru");
        tbl.addColumn("Hari");
        tbl.addColumn("Jam Masuk");
        tabel.setModel(tbl);
        try{
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM jadwal");
            while (res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("id"),
                res.getString("kodepel"),
                res.getString("mapel"),
                res.getString("kelas"),
                res.getString("nig"),
                res.getString("nama_guru"),
                res.getString("hari"),
                res.getString("jm"),
                });
            tabel.setModel(tbl);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
            
        }
        
    }
    
