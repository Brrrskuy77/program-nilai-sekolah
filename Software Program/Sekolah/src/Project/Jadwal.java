    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        try{
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM jadwal where "+"kodepel like '%"+txtcari.getText()+"%' OR "+"mapel like '%"+txtcari.getText()+"%'");
            DefaultTableModel tbl= new DefaultTableModel();
            tbl.addColumn("No. ID");
            tbl.addColumn("Kode Pelajaran");
            tbl.addColumn("Mata Pelajaran");
            tbl.addColumn("Kelas");
            tbl.addColumn("NIG");
            tbl.addColumn("Nama Guru");
            tbl.addColumn("Hari");
            tbl.addColumn("Jam Masuk");
            tabel.setModel(tbl);
            while(res.next()){
                tbl.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("kodepel"),
                    res.getString("mapel"),
                    res.getString("kelas"),
                    res.getString("nig"),
                    res.getString("nama_guru"),
                    res.getString("hari"),
                    res.getString("jm"),
                });
                tabel.setModel(tbl);
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(null ,"error");
        }
    }//GEN-LAST:event_bcariActionPerformed

